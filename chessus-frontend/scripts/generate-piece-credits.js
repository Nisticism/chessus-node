/**
 * Scans all README.txt files in src/assets/pieces/ and generates
 * a JS module with credit info per category/style.
 * 
 * Run: node scripts/generate-piece-credits.js
 */
const fs = require('fs');
const path = require('path');

const piecesDir = path.join(__dirname, '..', 'src', 'assets', 'pieces');
const outputFile = path.join(__dirname, '..', 'src', 'assets', 'piece-credits.js');

const credits = {};

const categories = fs.readdirSync(piecesDir, { withFileTypes: true })
  .filter(d => d.isDirectory() && d.name !== 'legacy');

for (const cat of categories) {
  const catPath = path.join(piecesDir, cat.name);
  const styles = fs.readdirSync(catPath, { withFileTypes: true })
    .filter(d => d.isDirectory());

  for (const style of styles) {
    const readmePath = path.join(catPath, style.name, 'README.txt');
    if (!fs.existsSync(readmePath)) continue;

    const text = fs.readFileSync(readmePath, 'utf-8');
    const lines = text.split('\n').map(l => l.trim()).filter(Boolean);

    let author = '';
    let variantBy = '';
    let license = '';
    let source = '';

    for (const line of lines) {
      if (line.match(/^Author:/i)) {
        author = line.replace(/^Author:\s*/i, '');
      } else if (line.match(/^Original author:/i)) {
        author = line.replace(/^Original author:\s*/i, '');
      } else if (line.match(/^Color variant by:/i)) {
        variantBy = line.replace(/^Color variant by:\s*/i, '');
      } else if (line.match(/^License:/i)) {
        license = line.replace(/^License:\s*/i, '');
      } else if (line.match(/^Source:/i)) {
        source = line.replace(/^Source:\s*/i, '');
      }
    }

    const key = `${cat.name}/${style.name}`;
    credits[key] = { author, variantBy, license, source };
  }
}

const output = `// Auto-generated by scripts/generate-piece-credits.js — do not edit manually
const pieceCredits = ${JSON.stringify(credits, null, 2)};

export default pieceCredits;
`;

fs.writeFileSync(outputFile, output, 'utf-8');
console.log(`Generated credits for ${Object.keys(credits).length} style folders → ${outputFile}`);
